#!/usr/bin/python3

def add():
    """Adding function
    Arguments:
            a->
            b->
    Return:
           add(a, b)
    """
    return (a + b)


def sub():
    """Subtract function
    Arguments:
            a->
            b->
    Return:
          subtract(a - b)
    """
    return (a - b)


def mul():
    """Multiplication function
    Argument:
           a->
           b->
    Return:
          multiply(a * b)
    """
    return(a * b)


def div():
    """Division function
    Argument:
           a->
           b->
    Return:
          divide( a / b)
    """
    return int(a / b)
